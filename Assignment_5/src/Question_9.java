/* Write a program to print cubes of the 10 natural numbers. */
public class Question_9 {
    public static void main(String[]args){
        System.out.println("Cubes of first 10 natural numbers is: ");
        for (int i=1;i<=10;i++)
        {
            System.out.println(i+"*"+i+"*"+i+"="+(i*i*i));
        }
    }
}
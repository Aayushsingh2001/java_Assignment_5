/* Write a program to print squares of the 10 natural numbers. */
public class Question_8 {
    public static void main(String[]args){
        System.out.println("Squares of 10 natural numbers is: ");
        for (int i=1;i<=10;i++)
        {
            System.out.println(i+"*"+i+"="+i*i);
        }
    }
}
